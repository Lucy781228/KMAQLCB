<template>
    <div v-if="!businesses.length">
        <NcEmptyContent>
            <template #title>
                <h1 class="empty-content__title">
                    Không có dữ liệu
                </h1>
            </template>
            <template #action>
                <NcButton ariaLabel="A" @click="startCreating" type="primary">
                    Thêm dữ liệu
                </NcButton>
            </template>
        </NcEmptyContent>
        <NewBusiness :modal="isAdd" :qlcb-uid="userId" @close="stopModal" />
    </div>
    <div v-else>
        <div class="table-actions">
            <NcButton type="tertiary" ariaLabel="A" @click="startCreating">
                <template #icon>
                    <Plus :size="20" />
                </template>
            </NcButton>
            <NcButton type="tertiary" ariaLabel="B" @click="deleteBusinesses">
                <template #icon>
                    <Delete :size="20" />
                </template>
            </NcButton>
        </div>
        <vue-good-table :columns="columns" :rows="businesses" :select-options="selectOptions"
            @on-selected-rows-change="selectionChanged" :pagination-options="{
        enabled: true,
        mode: 'records',
        perPage: 5,
        position: 'bottom',
        perPageDropdownEnabled: false,
        nextLabel: 'Sau',
        prevLabel: 'Trước',
        ofLabel: 'trên',
    }">
            <template slot="table-row" slot-scope="props">
                <span v-if="props.column.field === 'action'" @click="editBusiness(props.row)">
                    <Pencil :size="20" class="icon" />
                </span>
            </template>
        </vue-good-table>
        <NcModal :show="modal" @close="closeModal" :canClose="false">
            <div class="modal__content">
                <h3>Bạn chắc chắn không?</h3>
                <div class="modal__actions">
                    <NcButton @click="closeModal" type="primary">
                        Hủy
                    </NcButton>
                    <NcButton @click="onDeleteBusinesses" type="secondary">
                        Xóa
                    </NcButton>
                </div>
            </div>
        </NcModal>
        <NewBusiness :modal="isAdd" :qlcb-uid="userId" @close="stopModal" />
        <NewBusiness :modal="isEdit" :qlcb-uid="userId" @close="stopModal" :end-date="endDate"
            :position="position" :start-date="startDate" :unit="unit"
            :is-edit="true" :business-id="businessId" />
    </div>
</template>

<script>
import VueGoodTablePlugin from 'vue-good-table';
import 'vue-good-table/dist/vue-good-table.css';
import axios from "@nextcloud/axios";
import { generateUrl } from '@nextcloud/router'
import { NcButton, NcEmptyContent, NcModal } from "@nextcloud/vue";
import Plus from 'vue-material-design-icons/Plus'
import Pencil from 'vue-material-design-icons/Pencil'
import Download from 'vue-material-design-icons/Download'
import Delete from 'vue-material-design-icons/Delete'
import { showError, showSuccess } from '@nextcloud/dialogs'
import NewBusiness from './NewBusiness.vue'

export default {
    name: 'Business',
    components: {
        VueGoodTablePlugin,
        Plus,
        Delete,
        Download,
        NcButton,
        NcModal,
        NcEmptyContent,
        NewBusiness,
        Pencil
    },
    props: {
        userId: {
            type: String,
            required: true
        }
    },
    data() {
        return {
            isAdd: false,
            isEdit: false,
            columns: [
                {
                    label: 'Ngày bắt đầu',
                    field: 'start_date',
                    sortable: true,
                    width: '20%',
                    type: 'date',
                    dateInputFormat: 'yyyy-MM-dd',
                    dateOutputFormat: 'dd/MM/yyyy'
                },
                {
                    label: 'Ngày kết thúc',
                    field: 'end_date',
                    sortable: false,
                    width: '20%',
                    type: 'date',
                    dateInputFormat: 'yyyy-MM-dd',
                    dateOutputFormat: 'dd/MM/yyyy'
                },
                {
                    label: 'Đơn vị',
                    field: 'unit',
                    sortable: false,
                    width: '25%'
                },
                {
                    label: 'Chức vụ',
                    field: 'position',
                    sortable: false,
                    width: '30%'
                },
                {
                    label: 'Hành động',
                    field: 'action',
                    sortable: false,
                    width: '5%'
                },
            ],
            businesses: [],
            selectOptions: {
                enabled: true,
                disableSelectInfo: true,
                selectOnCheckboxOnly: true,
            },
            selectedBusinesses: null,
            modal: false,
            startDate: null,
            endDate: null,
            unit: "",
            position: "",
            businessId: ""
        };
    },

    mounted() {
        this.getBusinesses()
    },

    methods: {
        editBusiness(row) {
            this.startDate = row.start_date;
            this.endDate = row.end_date;
            this.unit = row.unit;
            this.position = row.position;
            this.businessId = row.business_id;

            this.isEdit = true;
        },
        startCreating() {
            this.isAdd = true
        },

        stopModal() {
            this.isAdd = false
            this.isEdit = false
            this.getBusinesses()
        },

        closeModal() {
            this.modal = false
        },

        deleteBusinesses() {
            if (!this.selectedBusinesses)
                showError(t('qlcb', 'Không có dòng nào được chọn'));
            else this.modal = true

        },

        async getBusinesses() {
            try {
                const response = await axios.get(generateUrl(`/apps/kmaqlcb/businesses/${this.userId}`));
                this.businesses = response.data.businesses
                console.log(this.businesses.length)

            } catch (e) {
                console.error(e)
            }
        },

        async onDeleteBusinesses() {
            try {
                const deletePromises = this.selectedBusinesses.map(business =>
                    axios.delete(generateUrl(`apps/kmaqlcb/delete_business/${business.business_id}`))
                );
                await Promise.all(deletePromises);
                await this.getBusinesses();
                showSuccess(t('qlcb', 'Xóa thành công'));
                this.modal = false
            } catch (e) {
                console.error('Error deleting selected educations:', e);
            }
        },

        selectionChanged({ selectedRows }) {
            if (selectedRows.length)
                this.selectedBusinesses = selectedRows
            else this.selectedBusinesses = null
            console.log(selectedRows);
        },
    }
};
</script>

